# ==========================================================
# Runtime Manifest
# ==========================================================
    

def load_animation_sequence(
    words: list[str]
) -> dict[str, np.ndarray]:
    """
    Load multiple runtime animations.
    """

    animations = {}

    for word in words:

        animations[word] = load_animation(word)

    return animations